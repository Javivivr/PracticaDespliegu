<h1 class="font-italic">Características de Java</h1>
<hr/>

<p class="font-weight-bolder">El lenguaje Java se creó con cinco objetivos principales:</p>
<ul>
    
    <li>
        Debería usar el paradigma de la programación orientada a objetos.
    
    </li>
    <li>
        Debería permitir la ejecución de un mismo programa en múltiples sistemas operativos.
    
    </li>
    <li>
        Debería incluir por defecto soporte para trabajo en red.
    
    </li>
    <li>
        Debería diseñarse para ejecutar código en sistemas remotos de forma segura.
    
    </li>
    <li>
        Debería ser fácil de usar y tomar lo mejor de otros lenguajes orientados a objetos, como C++.
    
    </li>
</ul>

<p>
Para conseguir la ejecución de código remoto y el soporte de red, los programadores de Java a veces recurren a extensiones como CORBA (Common Object Request Broker 
Architecture), Internet Communications Engine u OSGi, respectivamente.<br/><br/>
</p>

